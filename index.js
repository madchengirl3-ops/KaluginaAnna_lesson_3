let role = 'guest';

switch (role) {
    case 'admin':
        console.log('Добро пожаловать, администратор!');
        break;
    case 'manager':
        console.log('Привет, менеджер!');
        break;
    case 'user':
        console.log('Рады видеть вас снова!');
        break;
    case 'guest':
        console.log('Пожалуйста, войдите в систему!')
        break;
        
}

